// Helper function to get a random image from a category
export function getRandomImage(category: 'parts' | 'fixtures' | 'tools' | 'issues'): string {
  let images;
  
  switch (category) {
    case 'parts':
      images = plumbingPartImages;
      break;
    case 'fixtures':
      images = plumbingFixtureImages;
      break;
    case 'tools':
      images = plumbingToolImages;
      break;
    case 'issues':
      images = plumbingIssueImages;
      break;
    default:
      images = plumbingPartImages;
  }
  
  const randomIndex = Math.floor(Math.random() * images.length);
  return images[randomIndex].path;
}
